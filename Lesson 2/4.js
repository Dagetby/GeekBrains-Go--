'use strict';

function sum(arg1, arg2) {
    return arg1 + arg2;
}
function div(arg1, arg2) {
    return arg1 / arg2;
}
function mult(arg1, arg2) {
    return arg1 * arg2;
}
function sub(arg1, arg2) {
    return arg1 - arg2;
}

alert(sum(1, 2));
alert(div(1, 2));
alert(mult(1 ,2));
alert(sub(1, 2));


